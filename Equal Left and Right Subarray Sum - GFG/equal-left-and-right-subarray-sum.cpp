class Solution {
  public:
    int equalSum(int n, vector<int> &a) {
        long long sum = accumulate(a.begin(), a.end(),0);
        long long cur = 0;
        for(int i=0;i<n;i++){
            if(cur==sum-(cur+a[i])) return i+1;
            cur+=a[i];
        }
        return -1;
    }
};


